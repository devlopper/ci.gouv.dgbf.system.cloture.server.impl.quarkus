	"OPERATION" VARCHAR2(255 BYTE) NOT NULL ENABLE, 
	"OPERATION_DATE" DATE NOT NULL ENABLE, 
	"DECLENCHEUR" VARCHAR2(255 BYTE) NOT NULL ENABLE, 
